#include "../minitalk.h"

int	ft_isdigit(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (1);
		else
			return (0);
	}
	return (1);
}
